trapezoid_height = float(input("Enter the height of the trapezoid: "))
trapezoid_bottom = float(input("Enter the length of the bottom base: "))
trapezoid_top = float(input("Enter the length of the top base: "))

trapezoid_area = 0.5 * (trapezoid_bottom + trapezoid_top) * trapezoid_height

print("The area of this trapezoid is: ", trapezoid_area)